#include "libft.h"

int	ft_atoi(const char *str)
{
	int		s;
	int		res;

	s = 1;
	res = 0;
	while (*str != '\0' && (*str == '\v' || *str == '\n' || \
				*str == '\t' || *str == '\r' || *str == '\f' || *str == ' '))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			s *= -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		res = res * 10 + (*str++ - 48);
	}
	return (res * s);
}
